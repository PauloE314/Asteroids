/**
 * @param {Number} a
 * @param {Number} b
 * @returns {Number}
 */
export function getSmaller(a, b) {
    return a < b ? a : b;
}

/**
 * @param {Number} n
 */
export function radian(n) {
    return (Math.PI / 180) * n;
}

/**
 * @param {Number} n
 */
export function sin(n) {
    return Math.sin(n);
}

/**
 * @param {Number} n
 */
export function cos(n) {
    return Math.cos(n);
}
